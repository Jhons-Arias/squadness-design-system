// ─── Toast ───────────────────────────────────────────────────────────────────

/**
 * Notification toast with 5 semantic variants.
 *
 * @example
 * <Toast type="success">Los cambios fueron guardados correctamente.</Toast>
 * <Toast type="error">No se pudo conectar con el servidor.</Toast>
 */
function Toast({ type = 'default', children, className }: ToastProps) {
  const icon = ICONS[type]

  return (
    <div
      className={cn('sq-toast', `sq-toast--${type}`, className)}
      role="alert"
      aria-live="polite"
    >
      {icon && <span className="sq-toast-icon">{icon}</span>}
      <p className="sq-toast-message">{children}</p>
    </div>
  )
}

Toast.displayName = 'Toast'

